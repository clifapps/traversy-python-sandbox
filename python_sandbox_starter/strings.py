# Strings in python are surrounded by either single or double quotation marks. Let's look at string formatting and some string methods

name = 'Chris'
age = 64
# String Formatting
# f-strings (f is shorthand [an abstraction] for format)
# full method: print("Hello, my name is "+{name}+"and I am old: "+{age}.format(name=name, age=age))
# abstraction:
print(f"Hello, my name is {name} and I am old: {age}")


